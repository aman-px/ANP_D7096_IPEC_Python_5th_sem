# Inputs: Basic salary and performance incentive amount
basic_salary = float(input("Enter the Basic Salary: "))  # Data Type: float
incentive = float(input("Enter the Incentive amount: "))  # Data Type: float

# Calculation: Adding the fixed salary and incentive amount
total_salary = basic_salary + incentive  # Operator: +

# Output: Displaying the calculated total monthly salary
print("Total Monthly Salary:",total_salary)