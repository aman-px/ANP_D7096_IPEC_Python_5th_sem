# Function to calculate the sum of two numbers
def calculate_sum(num1, num2):
    return num1 + num2
# Function to calculate the difference of two numbers
def calculate_difference(num1, num2):
    return num1 - num2

# Function to calculate the product of two numbers
def calculate_product(num1, num2):
    return num1 * num2
# Function to calculate the quotient of two numbers
def calculate_division(num1, num2):
    return num1 / num2
# Function to calculate the remainder of a number
def calculate_remainder(num1, num2):
    return num1 % num2